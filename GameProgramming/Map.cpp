Map::Map() 
{
	ground = TextureManager::LoadTexture("assets/ground.png");
	wall = TextureManager::LoadTexture("assets/wall.png");
	lava = TextureManager::LoadTexture("assets/lava.png");

	loadMap(map1);
	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;
	dest.x = dest.y = 0;
}

Map::~Map()
{
	SDL_DestroyTexture(ground);
	SDL_DestroyTexture(wall);
	SDL_DestroyTexture(lava);
}

void Map::loadMap(int arr[20][25]) {
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::drawMap() {
	int type = 0;
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type) {
			case 0:
				TextureManager::draw(ground, src, dest);
				break;
			case 1:
				TextureManager::draw(wall, src, dest);
				break;
			case 2:
				TextureManager::draw(lava, src, dest);
				break;
			default:
				break;
			}
		}
	}
}